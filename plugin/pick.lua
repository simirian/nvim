--- generic picker plugin

--- @type table<thread, vim.SystemObj>
local procs = {}

--- @class Pick.Async
local Async = {
  --- @type thread
  co = nil,

  --- @type boolean?
  shouldabort = nil,

  --- @type fun(out: any)?
  ondone = nil,

  --- @type integer
  timeout = 0,
}

--- Creates a new async routine.
--- @param fn fun(): any
--- @return Pick.Async
function Async.new(fn)
  return setmetatable({ co = coroutine.create(fn) }, { __index = Async })
end

--- Runs the async routine.
function Async:run()
  if self.shouldabort then return end
  local ret = { coroutine.resume(self.co) }
  if not table.remove(ret, 1) then error(ret[1]) end
  local status = coroutine.status(self.co)
  if status == "dead" then
    if self.ondone then
      self.ondone(unpack(ret))
    end
  elseif status == "suspended" then
    vim.defer_fn(function() self:run() end, self.timeout)
  end
end

--- Aborts the async routine.
function Async:abort()
  if procs[self.co] then
    --- @diagnostic disable-next-line: undefined-field
    procs[self.co]:kill(vim.uv.constants.SIGTERM)
  end
  self.shouldabort = true
end

--- Registers a completion callback.
--- @param cb fun(any)
function Async:done(cb)
  self.ondone = cb
  return self
end

--- Sets a delay for subsequent calls to the function. USE WITH CARE.
--- @param ms integer The delay time in miliseconds.
function Async:delay(ms)
  self.timeout = ms
  return self
end

--- Runs a command with all callbacks wrapped as async functions.
--- @param cmd string[] The command to execute.
--- @param opts vim.SystemOpts Options.
function Async.system(cmd, opts)
  local out
  procs[coroutine.running()] = vim.system(cmd, opts, function(o) out = o end)
  while not out do
    coroutine.yield()
  end
  return out
end


--- @class Pick.Picker<T>
local Picker = {}

--- Creates a new picker from a table.
--- @param tbl table The table to turn into a picker.
--- @return Pick.Picker picker The new picker
function Picker.new(tbl)
  return setmetatable(tbl, { __index = Picker })
end

--- Generates the initial list for the picker to select out of. This will be run
--- in an async context, so `coroutine.yield()` can be used to prevent blocking
--- the main thread.
--- @generic T
--- @return T[] items The generated items.
function Picker:generate()
  return {}
end

--- Sorts the list that was generated. This will be run in an async context, so
--- `coroutine.yield()` can be used to prevent blocking the main thread.
--- @generic T
--- @param items T[] The list of items to sort.
--- @param prompt string The user prompt to use for sorting.
--- @return T[] items The sorted items
--- @diagnostic disable-next-line: unused-local
function Picker:sort(items, prompt)
  return items
end

--- Called to display each item which is shown in the list.
--- @generic T
--- @param item T The item to display.
--- @param idx integer The index of the number.
--- @return string str The item as a string.
--- @diagnostic disable-next-line: unused-local
function Picker:display(item, idx)
  return tostring(item)
end

--- Called when an item in the list is selected.
--- @generic T
--- @param item T The item that was selected.
--- @param idx integer The index of the item.
--- @diagnostic disable-next-line: unused-local
function Picker:confirm(item, idx)
  vim.print(item)
end

